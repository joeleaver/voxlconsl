const fn rgb(r: u8, g: u8, b: u8) -> PaletteColor {
    PaletteColor { r, g, b }
}

/// Apply lighting to a material's color: keep the ramp, replace the shade.
///
/// `shade` is clamped to 0..4. `material_color` is the cart's packed
/// `(ramp << 2) | shade` byte.
pub const fn lit_color_index(material_color: u8, shade: u8) -> u8 {
    (material_color & 0b1111_1100) | (if shade > 3 { 3 } else { shade })
}
